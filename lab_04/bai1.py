n = int(input("Nhập n: "))
while n <= 0:
    n = int(input("Nhập lại n > 0: "))
# a) S4 = 1^2 + 2^2 + ... + n^2
i = 1
S4 = 0
while i <= n:
    S4 += i * i
    i += 1
# b) S5 = 1^3 + 3^3 + ... + (2n+1)^3
i = 0
S5 = 0
while i <= n:
    S5 += (2*i + 1) ** 3
    i += 1
# c) S6 = 2^4 + 4^4 + ... + (2n)^4
i = 1
S6 = 0
while i <= n:
    S6 += (2*i) ** 4
    i += 1
print("S4 =", S4)
print("S5 =", S5)
print("S6 =", S6)